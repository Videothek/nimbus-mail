/** Search index built once at module load вАФ flattens emoji вЖТ
 *  search blob (keywords + category label) so the picker can
 *  do a cheap substring match per keystroke. */
export interface EmojiSearchEntry {
  emoji: string
  /** Lowercased searchable text: keywords plus category label. */
  search: string
}
export const EMOJI_SEARCH_INDEX: EmojiSearchEntry[] = (() => {
  const out: EmojiSearchEntry[] = []
  const seen = new Set<string>()
  for (const cat of EMOJI_CATEGORIES) {
    const catBlob = `${cat.label} ${cat.id}`.toLowerCase()
    for (const e of cat.emojis) {
      if (seen.has(e)) continue
      seen.add(e)
      const kw = (EMOJI_KEYWORDS[e] ?? '').toLowerCase()
      out.push({ emoji: e, search: `${kw} ${catBlob}` })
    }
  }
  return out
})()
